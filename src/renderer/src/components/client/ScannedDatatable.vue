<template>
    <DataTable :value="data" paginator :rows="5" :rowsPerPageOptions="[5, 10, 20, 100]" scrollable scrollHeight="flex"
        removableSort stripedRows v-model:filters="filters" :globalFilterFields="['name', 'type', 'filepath']"
        filterDisplay="row">

        <template #header>
            <div class="flex flex-wrap items-center justify-between gap-2">
                <span class="text-md text-gray-800 antialiased dark:text-surface-0 font-bold">{{ title }}</span>
                <div class="flex flex-row gap-2">
                    <span class="relative">
                        <i class="pi pi-search absolute top-2/4 -mt-2 left-3 text-surface-400 dark:text-surface-600 " />
                        <InputText v-model="filters['global'].value" placeholder="Search"
                            class="pl-2  rounded-sm font-normal " />
                    </span>
                    <!-- <Button icon="pi pi-refresh" rounded raised /> -->
                </div>
            </div>
        </template>


        <Column field="name" header="Name" sortable :showFilterMenu="false" style="width: 70%;">
            <template #body="slotProps">
                <p class="text-md font-sans text-slate-900 font-medium w-full truncate">
                    {{ slotProps.data.name }}
                </p>
            </template>
            <template #filter="{ filterModel, filterCallback }">
                <InputText v-model="filterModel.value" type="text" @input="filterCallback()"
                    class="p-column-filter font-normal text-sm uppercase rounded-sm" placeholder="Search By Name" />
            </template>
        </Column>

        <Column field="type" header="Type" sortable :showFilterMenu="false" style="width: 10%;">
            <template #body="{ data }">
                <div class="flex flex-row gap-1 w-full justify-center">
                    <!-- <Tag class="rounded" :value="data.type" :severity="getSeverity(data.type)" /> -->
                    <Tag :title="data.type" :class="getSeverity(data.type)" />
                </div>
            </template>

            <template #filter="{ filterModel, filterCallback }">
                <Dropdown v-model="filterModel.value" @change="filterCallback()" :options="types" placeholder="Type"
                    class="p-column-filter font-normal text-sm uppercase rounded-sm" style="width: 8rem;" :showClear="true">
                    <template #option="slotProps">
                        <Tag :title="slotProps.option" :class="getSeverity(slotProps.option)" />
                    </template>
                </Dropdown>
            </template>
        </Column>

        <Column field="year" header="Year" sortable :showFilterMenu="false" style="width: 10%;">
            <template #body="slotProps">
                <div class="w-full flex justify-center">
                    <p class="text-md text-gray-900 font-bold">{{ slotProps.data.year }}</p>
                </div>
            </template>

            <template #filter="{ filterModel, filterCallback }">
                <Dropdown v-model="filterModel.value" @change="filterCallback()" :options="year" placeholder="Year"
                    class="p-column-filter font-normal text-sm uppercase rounded-sm " style="width: 8rem;"
                    :showClear="true">
                    <template #option="slotProps">
                        <p class="text-md text-gray-900 font-bold">{{ slotProps.option }}</p>
                    </template>
                </Dropdown>
            </template>
        </Column>

        <Column header="Action" style="width: 10%;">
            <template #body="slotProps">
                <div class="flex items-center justify-center">
                    +
                </div>
            </template>
        </Column>

    </DataTable>
</template>

<script setup>
import DataTable from 'primevue/datatable';
import Column from 'primevue/column';
import Row from 'primevue/row';
import InputText from 'primevue/inputtext';
import Dropdown from 'primevue/dropdown';
import { FilterMatchMode } from "primevue/api";
import Tag from './Tag.vue'
import { ref, computed } from 'vue';


const props = defineProps({
    data: {
        type: Array,
        required: true
    }, title: {
        type: String,
        required: true
    },
    types: {
        type: Array,
        required: true,
    },
})

const year = computed(() => {
    return [...new Set(props.data.map(data => data.year))]
        .sort((a, b) => a - b);
});

const filters = ref({
    global: { value: null, matchMode: FilterMatchMode.CONTAINS },
    name: { value: null, matchMode: FilterMatchMode.CONTAINS },
    type: { value: null, matchMode: FilterMatchMode.EQUALS },
    year: { value: null, matchMode: FilterMatchMode.EQUALS },

});


const getSeverity = (type) => {
    switch (type) {
        case "Birth":
            return "bg-green-100";

        case "Death":
            return "bg-blue-100";

        case "Marriage":
            return "bg-red-100";

        case "Legal":
            return "bg-yellow-50";

        case "Other":
            return "bg-gray-100";
    }
};
</script>

